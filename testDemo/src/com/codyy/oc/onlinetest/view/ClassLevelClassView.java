package com.codyy.oc.onlinetest.view;

public class ClassLevelClassView {

	private String taskExamId;
	private String classLevalId;
	private String classLevalName;
	private String classId;
	private String className;
	public String getClassLevalId() {
		return classLevalId;
	}
	public void setClassLevalId(String classLevalId) {
		this.classLevalId = classLevalId;
	}
	public String getClassLevalName() {
		return classLevalName;
	}
	public void setClassLevalName(String classLevalName) {
		this.classLevalName = classLevalName;
	}
	public String getClassId() {
		return classId;
	}
	public void setClassId(String classId) {
		this.classId = classId;
	}
	public String getClassName() {
		return className;
	}
	public void setClassName(String className) {
		this.className = className;
	}
	public String getTaskExamId() {
		return taskExamId;
	}
	public void setTaskExamId(String taskExamId) {
		this.taskExamId = taskExamId;
	}
	
	
}
